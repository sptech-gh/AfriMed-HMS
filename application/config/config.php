// Dynamic base URL resolution.
//
// Priority order:
//   1. For real HTTP requests, derive base_url from the actual request
//      ($_SERVER['HTTP_HOST'] + scheme + script path). This guarantees that
//      the same install works correctly via http://localhost/hms-master/ AND
//      http://<LAN-IP>/hms-master/ without breaking assets, AJAX, or links.
//   2. For CLI / cron / non-HTTP contexts, use APP_BASE_URL (Apache SetEnv
//      or .env via index.php loader).
//   3. Fall back to a safe localhost default.
//
// This protects against the LAN-deployment trap where setting
// APP_BASE_URL=http://<LAN-IP>/hms-master/ silently breaks localhost access
// because all CSS/JS/anchor URLs would be hard-pinned to the LAN IP.
if (!empty($_SERVER['HTTP_HOST'])) {
    $isHttps = (
        (!empty($_SERVER['HTTPS']) && strtolower($_SERVER['HTTPS']) !== 'off')
        || (!empty($_SERVER['HTTP_X_FORWARDED_PROTO']) && strtolower($_SERVER['HTTP_X_FORWARDED_PROTO']) === 'https')
        || (!empty($_SERVER['SERVER_PORT']) && (int)$_SERVER['SERVER_PORT'] === 443)
    );
    $scheme = $isHttps ? 'https' : 'http';
    $host = $_SERVER['HTTP_HOST'];
    // Derive install path from SCRIPT_NAME (e.g. "/hms-master/index.php" -> "/hms-master/")
    $scriptName = isset($_SERVER['SCRIPT_NAME']) ? $_SERVER['SCRIPT_NAME'] : '/index.php';
    $installPath = rtrim(str_replace('\\', '/', dirname($scriptName)), '/') . '/';
    if ($installPath === '' || $installPath === '//') { $installPath = '/'; }
    $config['base_url'] = $scheme . '://' . $host . $installPath;
} elseif (getenv('APP_BASE_URL') !== false && getenv('APP_BASE_URL') !== '') {
    $config['base_url'] = rtrim(getenv('APP_BASE_URL'), '/') . '/';
} else {
    $config['base_url'] = 'http://localhost/hms-master/';
}

